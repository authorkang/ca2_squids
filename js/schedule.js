function populateSchedule(year, containerId) {
  const container = document.getElementById(containerId);
  container.innerHTML = "";

  for (const date in scheduleData) {
    if (date.includes(year)) {
      const matches = scheduleData[date];
      const dateSection = document.createElement("div");
      dateSection.classList.add("date-section", "card");

      const dateTitle = `
        <div class="date-title">
          <h5>${date}</h5>
        </div>
      `;

      const matchTable = `
        <div class="card-body">
          <table class="match-table">
            <thead>
              <tr>
                <th>Home Team</th>
                <th>Away Team</th>
                <th>Time</th>
                <th>Venue</th>
              </tr>
            </thead>
            <tbody>
              ${matches
                .map(
                  (match) => `
                <tr>
                  <td>${match.home}</td>
                  <td>${match.away}</td>
                  <td>${match.time}</td>
                  <td>${match.venue}</td>
                </tr>
              `
                )
                .join("")}
            </tbody>
          </table>
        </div>
      `;

      dateSection.innerHTML = dateTitle + matchTable;
      container.appendChild(dateSection);
    }
  }
}

